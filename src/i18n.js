export function makeT(locale) {
  return (key, params) => {
    const entry = dict[key]
    let str = entry ? (entry[locale] ?? entry.en ?? key) : key
    if (params) {
      for (const k in params) str = str.replaceAll(`{${k}}`, params[k])
    }
    return str
  }
}
